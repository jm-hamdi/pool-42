#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] <= 'Z') && (str[i] >= 'A'))
			i++;
		else
			return (0);
	}
	return (1);
}
